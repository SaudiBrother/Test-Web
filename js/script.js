// ===== FUNGSI UTILITAS =====
function updateCurrentDate() {
    try {
        const now = new Date();
        const options = { 
            weekday: 'long', 
            year: 'numeric', 
            month: 'long', 
            day: 'numeric' 
        };
        const formattedDate = now.toLocaleDateString('id-ID', options);
        const currentDateElement = document.getElementById('currentDate');
        if (currentDateElement) {
            currentDateElement.textContent = formattedDate;
        }
    } catch (error) {
        console.error('Error updating date:', error);
    }
}

function toggleTheme() {
    try {
        const body = document.body;
        body.classList.toggle('light-mode');
        body.classList.toggle('dark-mode');
        const isDarkMode = body.classList.contains('dark-mode');
        localStorage.setItem('theme', isDarkMode ? 'dark' : 'light');
    } catch (error) {
        console.error('Error toggling theme:', error);
    }
}

function setInitialTheme() {
    try {
        const savedTheme = localStorage.getItem('theme') || 'light';
        document.body.classList.add(savedTheme + '-mode');
        document.body.classList.remove(savedTheme === 'dark' ? 'light-mode' : 'dark-mode');
    } catch (error) {
        console.error('Error setting initial theme:', error);
        document.body.classList.add('light-mode');
    }
}

// ===== FUNGSI NAVIGASI LAYAR DENGAN ANIMASI SLIDE =====
function showScreen(screenName) {
    try {
        console.log('Showing screen:', screenName);
        
        const backContainer = document.getElementById('backContainer');
        
        // Sembunyikan tombol kembali di menu utama
        if (screenName === 'mainMenu') {
            backContainer.classList.remove('visible');
        } else {
            backContainer.classList.add('visible');
        }
        
        // Jika ada layar aktif sebelumnya, beri animasi keluar
        if (currentState.currentScreen && currentState.currentScreen !== screenName) {
            const previousScreen = screens[currentState.currentScreen];
            const newScreen = screens[screenName];
            
            // Tentukan arah animasi berdasarkan alur navigasi
            const navFlow = {
                'mainMenu': { next: 'slide-in-right', prev: 'slide-in-left' },
                'rombelSelection': { next: 'slide-in-right', prev: 'slide-in-left' },
                'daySelection': { next: 'slide-in-right', prev: 'slide-in-left' },
                'scheduleDisplay': { next: 'slide-in-right', prev: 'slide-in-left' },
                'funFactsDisplay': { next: 'slide-in-right', prev: 'slide-in-left' }
            };
            
            // Jika kembali ke sebelumnya
            const isGoingBack = (
                (currentState.currentScreen === 'rombelSelection' && screenName === 'mainMenu') ||
                (currentState.currentScreen === 'daySelection' && screenName === 'rombelSelection') ||
                (currentState.currentScreen === 'scheduleDisplay' && screenName === 'daySelection') ||
                (currentState.currentScreen === 'funFactsDisplay' && screenName === 'mainMenu')
            );
            
            // Animasi keluar untuk layar sebelumnya
            previousScreen.classList.add(isGoingBack ? 'slide-out-right' : 'slide-out-left');
            
            // Setelah animasi keluar selesai, sembunyikan layar
            setTimeout(() => {
                previousScreen.classList.remove('active');
                previousScreen.classList.remove('slide-out-left', 'slide-out-right');
                
                // Tampilkan layar baru dengan animasi masuk
                newScreen.classList.add('active');
                newScreen.classList.add(isGoingBack ? 'slide-in-left' : 'slide-in-right');
                
                // Setelah animasi masuk selesai, hapus kelas animasi
                setTimeout(() => {
                    newScreen.classList.remove('slide-in-left', 'slide-in-right');
                }, 400);
                
            }, 400);
            
        } else {
            // Untuk pertama kali, hanya tampilkan tanpa animasi
            Object.values(screens).forEach(screen => {
                if (screen) screen.classList.remove('active');
            });
            
            if (screens[screenName]) {
                screens[screenName].classList.add('active');
            }
        }
        
        currentState.previousScreen = currentState.currentScreen;
        currentState.currentScreen = screenName;
        
    } catch (error) {
        console.error('Error showing screen:', error);
    }
}

// ===== FUNGSI UNTUK MENAMPILKAN ROMBEL =====
function showRombels(className) {
    try {
        console.log('Showing rombels for class:', className);
        console.log('Available classes in data:', Object.keys(scheduleData));
        
        currentState.selectedClass = className;
        
        const rombelGrid = document.getElementById('rombelGrid');
        if (!rombelGrid) {
            console.error('Rombel grid element not found!');
            return;
        }
        
        rombelGrid.innerHTML = '';
        
        // Cek apakah data untuk kelas ini ada
        if (!scheduleData[className]) {
            console.error(`No data found for class ${className}`);
            rombelGrid.innerHTML = '<p class="no-data">Tidak ada data untuk kelas ini</p>';
            return;
        }
        
        const rombels = Object.keys(scheduleData[className]);
        console.log(`Found ${rombels.length} rombels for class ${className}:`, rombels);
        
        if (rombels.length === 0) {
            rombelGrid.innerHTML = '<p class="no-data">Tidak ada data rombel untuk kelas ini</p>';
            return;
        }
        
        // Urutkan rombel secara numerik
        rombels.sort((a, b) => parseInt(a) - parseInt(b));
        
        // Gunakan DocumentFragment untuk optimasi
        const fragment = document.createDocumentFragment();
        
        rombels.forEach((rombelNum, index) => {
            const rombelBtn = document.createElement('button');
            rombelBtn.className = 'rombel-btn';
            rombelBtn.textContent = `${className}-${rombelNum}`;
            rombelBtn.style.animationDelay = `${index * 0.05}s`;
            
            // Tambahkan event listener
            rombelBtn.addEventListener('click', function() {
                console.log('Rombel selected:', rombelNum);
                currentState.selectedRombel = rombelNum;
                showDays();
            });
            
            fragment.appendChild(rombelBtn);
        });
        
        rombelGrid.appendChild(fragment);
        
        // Trigger reflow untuk memulai animasi staggered
        setTimeout(() => {
            const buttons = rombelGrid.querySelectorAll('.rombel-btn');
            buttons.forEach((btn, i) => {
                btn.style.animationDelay = `${i * 0.05}s`;
            });
        }, 50);
        
        showScreen('rombelSelection');
    } catch (error) {
        console.error('Error showing rombels:', error);
    }
}

